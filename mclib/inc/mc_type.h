/**
 * @brief Enumeration defining motor control system error codes
 *
 * This enumeration represents all possible fault conditions in the motor control system.
 * Error codes use bitmask values to allow reporting multiple simultaneous faults.
 * Each code represents a specific category of system malfunction or protection trigger.
 */
typedef enum
{
  MC_NO_ERROR              = 0x00,   /**< No active errors (normal operation) */
  MC_OVER_VOLT_ERROR       = 0x01,   /**< DC bus overvoltage protection triggered */
  MC_UNDER_VOLT_ERROR      = 0x02,   /**< DC bus undervoltage protection triggered */
  MC_OVER_TEMP_ERROR       = 0x04,   /**< Overtemperature condition detected */
  MC_OVER_CURRENT_ERROR    = 0x08,   /**< Overcurrent protection triggered */
  MC_ENCODER_ERROR         = 0x10,   /**< Encoder fault condition detected */
  MC_HALL_ERROR            = 0x20,   /**< Hall sensor fault detected */
  MC_PARAM_IDENT_ERROR     = 0x40,   /**< Motor parameter identification failure  */
  MC_HALL_LEARN_ERROR      = 0x80,   /**< Hall sensor auto-learning procedure failed */
  MC_STARTUP_ERROR         = 0x100,  /**< Motor startup sequence failure
                                      *   (failed to achieve stable open-to-closed loop transition) */
} err_code_type;

