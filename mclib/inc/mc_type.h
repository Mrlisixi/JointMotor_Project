/**
  * @brief  PID Controller Configuration and Runtime Structure
  * @details Implements a full-featured fixed-point PID controller with:
  *          - Anti-windup protection
  *          - Gain scheduling
  *          - Fractional coefficient scaling
  *          - Output clamping
  *          Suitable for motor control and motion applications.
  */
typedef struct
{
  int16_t kp_gain;        /**< @brief Proportional gain (Q15 format) */
  int16_t ki_gain;        /**< @brief Integral gain (Q15 format) */
  int16_t kd_gain;        /**< @brief Derivative gain (Q15 format) */

  int32_t upper_limit_integral;  /**< @brief Maximum accumulated integral value */
  int32_t lower_limit_integral;  /**< @brief Minimum accumulated integral value */
  int16_t upper_limit_output;    /**< @brief Maximum controller output value */
  int16_t lower_limit_output;    /**< @brief Minimum controller output value */

  int32_t integral;      /**< @brief Current integral accumulator */
  int32_t error;         /**< @brief Current control error */
  int32_t pre_error;     /**< @brief Previous control error (for derivative term) */

  uint16_t kp_shift;     /**< @brief Right-shift bits for P term scaling */
  uint16_t ki_shift;     /**< @brief Right-shift bits for I term scaling */
  uint16_t kd_shift;     /**< @brief Right-shift bits for D term scaling */

  int16_t ki_gain_1st;   /**< @brief First integral gain (for position-control) */
  int16_t ki_gain_2nd;   /**< @brief Secondary integral gain (for position-control) */
  int32_t out_start;     /**< @brief Stores unclamped PID output */
} pid_ctrl_type;

