/**
 * @brief Current type definition
 *
 * This structure encapsulates various current measurements, transformations,
 * and related parameters essential for motor control applications.
 * It includes raw phase currents, their transformed components (alpha-beta, d-q),
 * current offsets, ADC configuration, and other calibration/limit values.
 */
typedef struct
{
  i_bus_type Ibus;                    /**< @brief DC bus current. */
  abc_type Iabc;                      /**< @brief Three-phase currents (Ia, Ib, Ic). */
  abc_type Iabc_shunt;                /**< @brief Three-phase currents measured via shunt resistors. */
  alphabeta_type Ialphabeta;          /**< @brief Alpha-beta components of the current (Clarke transformation). */
  qd_type Iqd;                        /**< @brief Direct-quadrature (d-q) components of the current (Park transformation). */
  qd_type Iqdref;                     /**< @brief Reference d-q current components. */
  qd_type Iqd_LPF;                    /**< @brief Low-pass filtered d-q current components. */
  curr_offs_type offset;              /**< @brief Current measurement offsets for calibration. */
  adc_type* ADCx;                     /**< @brief Pointer to the primary ADC instance for current sampling. */
  adc_type* ADC2x;                    /**< @brief Pointer to the secondary ADC instance for current sampling (if applicable). */
  adc_sampletime_select_type adc_sampletime; /**< @brief ADC sampling time configuration. */
  uint16_t span;                      /**< @brief Current measurement span or range. */
  uint16_t dc_span;                   /**< @brief DC current measurement span or range. */
  uint8_t span_shift;                 /**< @brief Bit shift value related to current span scaling. */
  int16_t nominal;                    /**< @brief Nominal current value. */
  int16_t max_current;                /**< @brief Maximum allowed current limit. */
  int16_t min_current;                /**< @brief Minimum allowed current limit. */
  int16_t volt_sign;                  /**< @brief Sign indicator for voltage, possibly related to current direction. */
  int16_t volt_sign_coming;           /**< @brief Pending sign indicator for voltage. */
  i_dc_type Idc;                      /**< @brief DC current measurement. */
  int16_t ID_1shunt;                  /**< @brief Direct current component when using a single shunt resistor measurement. */
  int16_t Isref;                      /**< @brief Reference stator current. */
  int32_t Iqd_squr_sum_max;           /**< @brief Maximum allowed sum of squared d-q currents (for current limit calculations). */
  int16_t Tref;                       /**< @brief Reference temperature, possibly for current derating. */
  tmr_type* adc_TMRx;                 /**< @brief Pointer to the timer instance controlling ADC triggering. */
  tmr_channel_select_type adc_tmr_trig_ch; /**< @brief Timer channel used for ADC trigger signal. */
  int16_t adc_trig_normal;            /**< @brief Normal ADC trigger point. */
  int16_t adc_trig_lagging;           /**< @brief Lagging ADC trigger point. */
} current_type;

