/**
 * @brief Enumeration defining general calibration process states
 *
 * This enumeration represents the various stages and outcomes of system calibration procedures,
 * including but not limited to:
 * - Encoder alignment process
 * - Current loop PI parameter calibration
 * - Other system tuning procedures
 *
 * @note The state machine should handle transitions between these states appropriately
 *       for different calibration types.
 */
typedef enum
{
  UNDO                     = 0x00, /**< Calibration procedure has not been initiated (idle state) */
  PROCESSING               = 0x01, /**< Calibration procedure is actively running */
  FINISHED                 = 0x02, /**< Calibration completed without success/failure status
                                   (neutral completion state) */
  SUCCEED                  = 0x03, /**< Calibration completed successfully with verified results */
  FAILED                   = 0x04  /**< Calibration procedure failed due to error or timeout */
} process_state_type;

