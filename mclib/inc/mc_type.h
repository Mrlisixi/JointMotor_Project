/**
 * @brief Structure for initial angle estimation parameters in sensorless BLDC motor control.
 *
 * This structure encapsulates all the necessary parameters and state variables
 * required for the robust and accurate initial rotor angle estimation
 * in a sensorless Brushless DC (BLDC) motor drive. It integrates timer configurations,
 * measurement duty cycles, step counters, current feedback, and calculated differences
 * to establish the motor's starting position without physical sensors.
 */
typedef struct
{
  tmr_type* ADVTMRx;       /**< @brief Pointer to the advanced timer used for PWM generation and commutation control. */
  tmr_type* ADC_TMRx;      /**< @brief Pointer to the timer triggering Analog-to-Digital Converter (ADC) measurements. */
  uint16_t tmr_period;     /**< @brief The period of the main timer, typically dictating the PWM frequency. */
  uint16_t detect_duty;    /**< @brief The duty cycle used during the initial angle detection phase. */
  int16_t step_count;      /**< @brief Counter for the steps or phases in the initial angle detection sequence. */
  init_current_type current; /**< @brief Structure holding the phase-to-phase current measurements during detection. */
  int16_t difference;      /**< @brief Calculated difference used for angle determination. */
} angle_init_type;

