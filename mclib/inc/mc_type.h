/**
 * @brief Defines the states of the Hall sensor self-learning process.
 *
 * This enumeration outlines the distinct stages of the automatic
 * learning procedure for a motor's Hall sensors. It guides the system
 * through a sequence of operations to determine the correct commutation
 * logic for the motor.
 */
typedef enum
{
  PROCESS_0_LOCK     = 0x00,
  /**< The initial state where the motor is held in a fixed position (locked). */
  PROCESS_1_FREE_RUN = 0x01,
  /**< The motor is free to run with no PWM output, remaining at a standstill. */
  PROCESS_2_OPENLOOP = 0x02,
  /**< A specific control voltage is applied in an open-loop configuration to start rotation. */
  PROCESS_3_LEARNING = 0x03,
  /**< The main learning phase where Hall sensor transitions are actively recorded. */
  PROCESS_4_FINISH   = 0x04,
  /**< The learning process has been completed successfully. */
  PROCESS_5_ERROR    = 0x05
                       /**< An error has occurred, and the learning process has failed. */
} hall_learn_process_type;

/**
  * @brief  PWM input calibration state machine
  * @details Defines the step-by-step process for calibrating PWM input ranges
  *          and calculating speed curve parameters for motor control
  */
typedef enum
{
  PROCESS_0_WAITING          = 0x00, /**< Initial idle state (waiting for calibration trigger) */
  PROCESS_1_CATCH_ZERO       = 0x01, /**< Calibration initialization state */
  PROCESS_2_CATCH_MAX        = 0x02, /**< Capturing maximum PWM width (full forward) */
  PROCESS_3_CATCH_ZERO       = 0x03, /**< Capturing neutral/zero PWM width */
  PROCESS_4_CATCH_MIN        = 0x04, /**< Capturing minimum PWM width (full reverse) */
  PROCESS_5_CATCH_ZERO       = 0x05, /**< Final neutral/zero position verification */
  PROCESS_6_SUCCESS          = 0x06  /**< Calibration completed successfully */
} calibrate_process_type;

